<!-- Relational Operators in php -->
<?php

   $a=10;
   $b=2;

   $c = $a > $b;               // greater than
   echo ($c);
   echo "</br>";

   $a=5;
   $b=4;

   $c = $a >= $b;             // greater than or equals to
   echo ($c);
   echo "</br>";

   $a=6;
   $b=4;

   $c = $a < $b;             // less than
   echo ($c);
   echo "</br>";

   $a=4;
   $b=4;

   $c = $a <= $b;            // less than or equals to
   echo ($c);
   echo "</br>";

   $a=8;
   $b=8;

   $c = $a == $b;            // is equals to
   echo ($c);
   echo "</br>";

   $a=5;
   $b=4;

   $c = $a != $b;            // is not equals to
   echo ($c);
   echo "</br>";

?>